package com.example.hola_prueba_tfg;

public class ReadWriteUserDetails {

    public String textNombre, textEmail,  textTelefono, textFecha, textPassword;

    public ReadWriteUserDetails() {};

    public ReadWriteUserDetails(String textNombre, String textEmail, String textTelefono, String textFecha, String textPassword) {
        this.textNombre = textNombre;
        this.textEmail = textEmail;
        this.textTelefono = textTelefono;
        this.textFecha = textFecha;
        this.textPassword = textPassword;
    }


}

